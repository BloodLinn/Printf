#include "printf.h"

int	ft_putptr(unsigned long a)
{
	char	*hex;
	int		i;
	
	i = 0;
	hex = "0123456789abcdef";
	if (a >= 16)
		i += ft_putptr(a / 16);
	i += ft_putchar(hex[a % 16]);
	return (i);
}
